def generate_analysis_pdf(
    startup_name: str,
    analysis_text: str,
    market_text: str,
    action_plan: list[str],
) -> bytes:
    """Generate a styled analysis report PDF. Returns raw bytes."""
    pdf = _BasePDF(title=f"{startup_name} — Analysis Report")
    pdf.add_page()

    # Title
    pdf.set_font("Helvetica", "B", 22)
    pdf.set_text_color(*_ACCENT)
    pdf.cell(0, 12, startup_name, new_x="LMARGIN", new_y="NEXT", align="C")
    pdf.set_font("Helvetica", "", 11)
    pdf.set_text_color(*_MUTED)
    pdf.cell(0, 8, "Startup Analysis Report", new_x="LMARGIN", new_y="NEXT", align="C")
    pdf.ln(10)

    # Analysis
    pdf._section_title("Detailed Startup Analysis")
    pdf._body_text(_clean_markdown(analysis_text))

    # Market Research
    pdf.add_page()
    pdf._section_title("Market Research Insights")
    pdf._body_text(_clean_markdown(market_text))

    # Action Plan
    if action_plan:
        pdf.add_page()
        pdf._section_title("Recommended Action Plan")
        for i, item in enumerate(action_plan, 1):
            pdf.set_font("Helvetica", "", 10)
            pdf.set_text_color(*_WHITE)
            pdf.multi_cell(0, 6, f"  {i}.  {item}")
            pdf.ln(2)

    # Footer
    pdf.set_font("Helvetica", "I", 8)
    pdf.set_text_color(*_MUTED)
    pdf.cell(0, 10, "Generated by AI Startup Builder", align="C")

    return pdf.output()


def generate_pitch_deck_pdf(
    startup_name: str,
    pitch_deck_text: str,
) -> bytes:
    """Generate a styled pitch deck PDF. Returns raw bytes."""
    pdf = _BasePDF(title=f"{startup_name} — Pitch Deck")

    # Parse slides from the pitch deck text
    slides = _parse_slides(pitch_deck_text)

    for i, slide in enumerate(slides):
        pdf.add_page()

        if i == 0:
            # Title slide — centered, large
            pdf.ln(60)
            pdf.set_font("Helvetica", "B", 28)
            pdf.set_text_color(*_ACCENT)
            pdf.cell(0, 14, startup_name, new_x="LMARGIN", new_y="NEXT", align="C")
            pdf.ln(4)
            pdf.set_font("Helvetica", "", 13)
            pdf.set_text_color(*_WHITE)
            tagline = slide.get("content", "").strip()
            if tagline:
                pdf.multi_cell(0, 7, tagline, align="C")
            pdf.ln(20)
            pdf.set_font("Helvetica", "I", 9)
            pdf.set_text_color(*_MUTED)
            pdf.cell(0, 8, "AI-Generated Pitch Deck", new_x="LMARGIN", new_y="NEXT", align="C")
        else:
            # Content slide
            pdf._section_title(slide.get("title", f"Slide {i + 1}"))
            pdf._body_text(_clean_markdown(slide.get("content", "")))

        # slide number
        pdf.set_y(280)
        pdf.set_font("Helvetica", "", 8)
        pdf.set_text_color(*_MUTED)
        pdf.cell(0, 5, f"{i + 1} / {len(slides)}", align="R")

    return pdf.output()


def _parse_slides(text: str) -> list[dict]:
    """Parse pitch deck text into slides. Expects ## headings as slide separators."""
    slides = []
    current_title = ""
    current_lines = []

    for line in text.split("\n"):
        stripped = line.strip()
        if stripped.startswith("## ") or stripped.startswith("# "):
            if current_title or current_lines:
                slides.append({
                    "title": current_title,
                    "content": "\n".join(current_lines).strip(),
                })
            current_title = stripped.lstrip("# ").strip()
            current_lines = []
        else:
            current_lines.append(line)

    if current_title or current_lines:
        slides.append({
            "title": current_title,
            "content": "\n".join(current_lines).strip(),
        })

    # If no slides parsed, treat entire text as one slide
    if not slides:
        slides = [{"title": "Overview", "content": text.strip()}]

    return slides
